info() {
  printf '\n%s\n' "$*"
}

success() {
  printf '✓ %s\n' "$*"
}

warn() {
  printf 'Warning: %s\n' "$*" >&2
}

die() {
  printf 'Error: %s\n' "$*" >&2
  exit 1
}

prompt_value() {
  local prompt="$1"
  local default_value="${2:-}"
  local answer=""

  if [[ -n "$default_value" ]]; then
    printf '%s [%s]: ' "$prompt" "$default_value" >&2
  else
    printf '%s: ' "$prompt" >&2
  fi
  IFS= read -r answer || die "Input stream closed before an answer was provided."
  printf '%s' "${answer:-$default_value}"
}

prompt_secret() {
  local prompt="$1"
  local answer=""
  printf '%s: ' "$prompt" >&2
  IFS= read -r -s answer || die "Input stream closed before a secret was provided."
  printf '\n' >&2
  printf '%s' "$answer"
}

confirm() {
  local prompt="$1"
  local answer=""
  printf '%s [y/N]: ' "$prompt" >&2
  IFS= read -r answer || die "Input stream closed before an answer was provided."
  case "$answer" in
    y|Y|yes|YES) return 0 ;;
    *) return 1 ;;
  esac
}

