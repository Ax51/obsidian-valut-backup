# ====== 3. Command-line arguments ============================================

parse_arguments() {
  while (($#)); do
    case "$1" in
      --source)
        (($# >= 2)) || die "--source requires a path."
        CLI_SOURCE_PATH="$2"
        shift 2
        ;;
      --no-immediate-backup)
        IMMEDIATE_BACKUP=false
        shift
        ;;
      --no-schedule)
        INSTALL_SCHEDULE=false
        shift
        ;;
      --update-settings)
        UPDATE_SETTINGS=true
        shift
        ;;
      --verify)
        VERIFY_AFTER_BACKUP=true
        shift
        ;;
      --restore)
        RESTORE_MODE=true
        IMMEDIATE_BACKUP=false
        INSTALL_SCHEDULE=false
        shift
        ;;
      --inspect)
        INSPECT_STATUS=true
        shift
        ;;
      --scheduled-run)
        SCHEDULED_RUN=true
        INSTALL_SCHEDULE=false
        shift
        ;;
      --version)
        printf '%s %s\n' "$APP_NAME" "$SCRIPT_VERSION"
        exit 0
        ;;
      -h|--help)
        usage
        exit 0
        ;;
      *)
        die "Unknown option: $1. Run with --help to see available options."
        ;;
    esac
  done
}

validate_argument_combinations() {
  if [[ "$RESTORE_MODE" == true ]]; then
    [[ "$SCHEDULED_RUN" == false ]] || die "--restore cannot run through launchd."
    [[ "$VERIFY_AFTER_BACKUP" == false ]] || die \
      "--restore cannot be combined with --verify."
    [[ "$INSPECT_STATUS" == false ]] || die \
      "--restore cannot be combined with --inspect."
    [[ "$UPDATE_SETTINGS" == false ]] || die \
      "Update settings in a separate run before using --restore."
  fi
}


